package frc.lib.io.hopper;

/**
 * Hardware abstraction for the hopper motor.
 */
public interface HopperIO {

    /** Populate the inputs struct with the latest sensor data. */
    void updateInputs(HopperInputs inputs);

    /** Set the hopper to a target position in degrees. */
    void setPosition(double position);

    boolean getMagSensor();

    void runPower(double power);
    
    /** Stop the hopper motor. */
    void hopperStop();
}
