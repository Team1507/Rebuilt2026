package frc.lib.io.intakeroller;

/**
 * Hardware abstraction for the intake roller motor.
 */
public interface IntakeRollerIO {

    /** Populate the inputs struct with the latest sensor data. */
    void updateInputs(IntakeRollerInputs inputs);

    /** Run the roller at a duty cycle (-1 to 1). */
    void runDuty(double duty);

    /** Run the roller in open-loop power mode. */
    void runPower(double power);

    /** Stop the roller. */
    void stop();
}
