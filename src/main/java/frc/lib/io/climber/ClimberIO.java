package frc.lib.io.climber;

/**
 * Hardware abstraction for the climber motor + servo.
 */
public interface ClimberIO {

    /** Populate the inputs struct with the latest sensor data. */
    void updateInputs(ClimberInputs inputs);

    /** Set the climber mechanism position (after gear ratio). */
    void setPosition(double mechanismPosition);

    /** Set the servo position (0–1). */
    void setServo(double position);

    /** Stop the climber motor. */
    void stop();
}
